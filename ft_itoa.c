#include "libft.h"

char *ft_itoa(int n)
{
    char *str;

    if (n == -2147483648)
	{
		str = "-2147483648";
		return;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_itoa(n / 10);
	n = (n % 10) + '0';
	return (str);
}